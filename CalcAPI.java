package com.bss;

public class CalcAPI {
	int res;
	public int add(int a, int b)
	{
		res=a+b;
		return res;
	}
	
	public int sub(int a, int b)
	{
		res=a-b;
		return res;
	}
	
	public int mult(int a, int b)
	{
		res=a*b;
		return res;
	}
	
	public int div(int a, int b)
	{
		res=a/b;
		return res;
	}

}
